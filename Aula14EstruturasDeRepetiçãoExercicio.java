package JavaCursoIntrodução;

public class Aula14EstruturasDeRepetiçãoExercicio {
    public static void main(String[] args) {
        // Imprimir os primeiros 25 numeros de 50
        int valorMax = 50;
        for (int i = 0; i <= valorMax ; i++) {
            if (i > 25){
                break;
            }
            System.out.println(i);
        }


    }
}