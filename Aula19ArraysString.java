package JavaCursoIntrodução;

public class Aula19ArraysString {
    public static void main(String[] args) {
        String[] nomes = new String[5];

        nomes[0] = "Carlos";
        nomes[1] = "Tadeu";
        nomes[2] = "Dias";
        nomes[3] = "Ramalho";
        nomes[4] = "CT";

        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);

        }
    }
}
